TRANSLATIONS = {"ru": RU, "en": EN}


def get_locale_from_cookie(cookies) -> str:
    if cookies is None:
        return "ru"
    try:
        raw = cookies.get("locale") or "ru"
        locale = (raw if isinstance(raw, str) else "ru").strip().lower()
        return "en" if locale == "en" else "ru"
    except Exception:
        return "ru"


def get_tr(locale: str) -> dict:
    return TRANSLATIONS.get(locale, RU)
